package pkgShape;

/*
 * Shape - abstract class
 */
public abstract class Shape {
	
	/*
	 * Shape constructor
	 */
	public Shape() {
		
	}
	
	
	/*
	 * area - abstract method for area of a Shape
	 */
	public abstract double area();
	
	
	/*
	 * perimeter - abstract method for perimeter of a Shape
	 */
	public abstract double perimeter();
	
}
